a = 4
b = 2

resultado = a == b  # SON INGUALES SI O NO
print(f"Resultado :{resultado}")

resultado = a != b  # SON DIFERENTES SI O NO
print(f"Resultado :{resultado}")

resultado = a > b  # A ES MAYOR DE B
print(f"Resultado :{resultado}")

resultado = a < b  # A ES MENOR DE B
print(f"Resultado :{resultado}")

resultado = a <= b  # A ES MENOR O IGUAL QUE B
print(f"Resultado :{resultado}")

resultado = a >= b  # A ES MAYOU O IGUAL A B
print(f"Resultado :{resultado}")
